public class UserResponse {
    private int id;
    private String name;
    private String username;
    private String email;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }
}
